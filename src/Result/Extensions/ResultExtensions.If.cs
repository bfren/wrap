using System;
using System.Threading.Tasks;

namespace Wrap;

public static partial class ResultExtensions
{
	/// <summary>
	/// Return a value determined by the value of <paramref name="this"/> and the result of <paramref name="predicate"/>.
	/// </summary>
	/// <typeparam name="T">Ok value type.</typeparam>
	/// <param name="this">Result object.</param>
	/// <param name="predicate">Uses value of <paramref name="this"/> to determin whether to return <paramref name="okTrue"/> or original value.</param>
	/// <param name="okTrue">Used if <paramref name="this"/> is <see cref="Ok{T}"/> and <paramref name="predicate"/> returns <see langword="true"/>.</param>
	/// <returns>Value generated by <paramref name="okTrue"/> or original value if <paramref name="predicate"/> is false.</returns>
	public static Result<T> If<T>(this Result<T> @this, Func<T, bool> predicate, Func<T, Result<T>> okTrue) =>
		Bind(@this,
			x => predicate(x) switch
			{
				false =>
					x,

				true =>
					okTrue(x)
			}
		);

	/// <inheritdoc cref="If{T}(Result{T}, Func{T, bool}, Func{T, Result{T}})"/>
	public static Task<Result<T>> IfAsync<T>(this Result<T> @this, Func<T, bool> predicate, Func<T, Task<Result<T>>> okTrue) =>
		BindAsync(@this,
			x => predicate(x) switch
			{
				false =>
					@this.AsTask(),

				true =>
					okTrue(x)
			}
		);

	/// <inheritdoc cref="If{T}(Result{T}, Func{T, bool}, Func{T, Result{T}})"/>
	public static Task<Result<T>> IfAsync<T>(this Task<Result<T>> @this, Func<T, bool> predicate, Func<T, Result<T>> okTrue) =>
		BindAsync(@this,
			x => predicate(x) switch
			{
				false =>
					@this,

				true =>
					okTrue(x).AsTask()
			}
		);

	/// <inheritdoc cref="If{T}(Result{T}, Func{T, bool}, Func{T, Result{T}})"/>
	public static Task<Result<T>> IfAsync<T>(this Task<Result<T>> @this, Func<T, bool> predicate, Func<T, Task<Result<T>>> okTrue) =>
		BindAsync(@this,
			x => predicate(x) switch
			{
				false =>
					@this,

				true =>
					okTrue(x)
			}
		);

	/// <summary>
	/// Return a value determined by the value of <paramref name="this"/> and the result of <paramref name="predicate"/>.
	/// </summary>
	/// <typeparam name="T">Ok value type.</typeparam>
	/// <typeparam name="TReturn">Return value type.</typeparam>
	/// <param name="this">Result object.</param>
	/// <param name="predicate">Uses value of <paramref name="this"/> to determines whether <paramref name="okFalse"/> or <paramref name="okTrue"/> should be used.</param>
	/// <param name="okTrue">Used if <paramref name="this"/> is <see cref="Ok{T}"/> and <paramref name="predicate"/> returns <see langword="true"/>.</param>
	/// <param name="okFalse">Used if <paramref name="this"/> is <see cref="Ok{T}"/> and <paramref name="predicate"/> returns <see langword="false"/>.</param>
	/// <returns>Value generated by<paramref name="okFalse"/> or <paramref name="okTrue"/>.</returns>
	public static Result<TReturn> If<T, TReturn>(this Result<T> @this, Func<T, bool> predicate, Func<T, Result<TReturn>> okTrue, Func<T, Result<TReturn>> okFalse) =>
		Bind(@this,
			x => predicate(x) switch
			{
				false =>
					okFalse(x),

				true =>
					okTrue(x)
			}
		);

	/// <inheritdoc cref="If{T, TReturn}(Result{T}, Func{T, bool}, Func{T, Result{TReturn}}, Func{T, Result{TReturn}})"/>
	public static Task<Result<TReturn>> IfAsync<T, TReturn>(this Result<T> @this, Func<T, bool> predicate, Func<T, Task<Result<TReturn>>> okTrue, Func<T, Task<Result<TReturn>>> okFalse) =>
		BindAsync(@this,
			x => predicate(x) switch
			{
				false =>
					okFalse(x),

				true =>
					okTrue(x)
			}
		);

	/// <inheritdoc cref="If{T, TReturn}(Result{T}, Func{T, bool}, Func{T, Result{TReturn}}, Func{T, Result{TReturn}})"/>
	public static Task<Result<TReturn>> IfAsync<T, TReturn>(this Task<Result<T>> @this, Func<T, bool> predicate, Func<T, Result<TReturn>> okTrue, Func<T, Result<TReturn>> okFalse) =>
		BindAsync(@this,
			x => predicate(x) switch
			{
				false =>
					okFalse(x),

				true =>
					okTrue(x)
			}
		);

	/// <inheritdoc cref="If{T, TReturn}(Result{T}, Func{T, bool}, Func{T, Result{TReturn}}, Func{T, Result{TReturn}})"/>
	public static Task<Result<TReturn>> IfAsync<T, TReturn>(this Task<Result<T>> @this, Func<T, bool> predicate, Func<T, Task<Result<TReturn>>> okTrue, Func<T, Task<Result<TReturn>>> okFalse) =>
		BindAsync(@this,
			x => predicate(x) switch
			{
				false =>
					okFalse(x),

				true =>
					okTrue(x)
			}
		);
}
