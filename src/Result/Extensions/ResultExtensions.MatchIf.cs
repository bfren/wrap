	/// <summary>
	/// Return a value determined by the value of <paramref name="this"/> and the result of <paramref name="predicate"/>.
	/// </summary>
	/// <typeparam name="T">Ok value type.</typeparam>
	/// <typeparam name="TReturn">Return value type.</typeparam>
	/// <param name="this">Result object.</param>
	/// <param name="fail">Function to return a value if <paramref name="this"/> is <see cref="Failure"/>.</param>
	/// <param name="predicate">Uses value of <paramref name="this"/> to determines whether <paramref name="okFalse"/> or <paramref name="okTrue"/> should be used.</param>
	/// <param name="okFalse">Used if <paramref name="this"/> is <see cref="Ok{T}"/> and <paramref name="predicate"/> returns <see langword="false"/>.</param>
	/// <param name="okTrue">Used if <paramref name="this"/> is <see cref="Ok{T}"/> and <paramref name="predicate"/> returns <see langword="true"/>.</param>
	/// <returns>Value generated by <paramref name="fail"/>, <paramref name="okFalse"/> or <paramref name="okTrue"/>.</returns>
	public static TReturn MatchIf<T, TReturn>(this Result<T> @this,
		Func<FailureValue, TReturn> fail,
		Func<T, bool> predicate,
		Func<T, TReturn> okFalse,
		Func<T, TReturn> okTrue
	) =>
		Match(@this,
			fail: fail,
			ok: x => predicate(x) switch
			{
				false =>
					okFalse(x),

				true =>
					okTrue(x)
			}
		);

	/// <inheritdoc cref="MatchIf{T, TReturn}(Result{T}, Func{FailureValue, TReturn}, Func{T, bool}, Func{T, TReturn}, Func{T, TReturn})"/>
	public static Task<TReturn> MatchIfAsync<T, TReturn>(this Result<T> @this,
		Func<FailureValue, TReturn> fail,
		Func<T, bool> predicate,
		Func<T, Task<TReturn>> okFalse,
		Func<T, TReturn> okTrue
	) =>
		MatchIfAsync(Task.FromResult(@this),
			fail: f => Task.FromResult(fail(f)),
			predicate: x => Task.FromResult(predicate(x)),
			okFalse: okFalse,
			okTrue: x => Task.FromResult(okTrue(x))
		);

