using System;
using System.Threading.Tasks;
using Wrap.Exceptions;

namespace Wrap.Extensions;

public static partial class ResultExtensions
{
	#region Throw Exception on Fail

	/// <summary>
	/// Unwrap the value contained in <paramref name="this"/> - throws an exception if the result is <see cref="Failure"/>.
	/// </summary>
	/// <typeparam name="T">Ok value type.</typeparam>
	/// <param name="this">Result object.</param>
	/// <returns>The value of <paramref name="this"/> or throws an exception.</returns>
	/// <exception cref="FailureException"></exception>
	public static T Unwrap<T>(this Result<T> @this) =>
		R.Match(@this,
			fail: R.ThrowFailure<T>,
			ok: x => x
		);

	/// <inheritdoc cref="Unwrap{T}(Result{T})"/>
	public static Task<T> UnwrapAsync<T>(this Task<Result<T>> @this)
	{
		// use local functions to avoid function signature ambiguity
		static Task<T> fail(FailureValue f) => R.ThrowFailure<Task<T>>(f);
		static Task<T> ok(T x) => Task.FromResult(x);
		return R.MatchAsync(@this, fail, ok);
	}

	#endregion

	#region Return Value on Fail

	/// <summary>
	/// Unwrap the value contained in <paramref name="this"/>.
	/// </summary>
	/// <remarks>
	/// <para>
	/// Provides access to the value wrapped by a <see cref="Result{T}"/> object.
	/// </para>
	/// <para>
	/// You need to provide a default value via <paramref name="ifFail"/> in case
	/// <paramref name="this"/> is <see cref="Failure"/>.
	/// </para>
	/// </remarks>
	/// <typeparam name="T">Ok value type.</typeparam>
	/// <param name="this">Result object.</param>
	/// <param name="ifFail">Function to generate a value if <paramref name="this"/> is <see cref="Failure"/>.</param>
	/// <returns>The value of <paramref name="this"/> or generated by <paramref name="ifFail"/>.</returns>
	public static T Unwrap<T>(this Result<T> @this, Func<FailureValue, T> ifFail) =>
		R.Match(@this,
			fail: ifFail,
			ok: x => x
		);

	/// <inheritdoc cref="Unwrap{T}(Result{T}, Func{FailureValue,T})"/>
	public static Task<T> UnwrapAsync<T>(this Result<T> @this, Func<FailureValue, Task<T>> ifFail) =>
		R.MatchAsync(@this,
			fail: ifFail,
			ok: x => x
		);

	/// <inheritdoc cref="Unwrap{T}(Result{T}, Func{FailureValue,T})"/>
	public static Task<T> UnwrapAsync<T>(this Task<Result<T>> @this, Func<FailureValue, T> ifFail) =>
		R.MatchAsync(@this,
			fail: ifFail,
			ok: x => Task.FromResult(x)
		);

	/// <inheritdoc cref="Unwrap{T}(Result{T}, Func{FailureValue,T})"/>
	public static Task<T> UnwrapAsync<T>(this Task<Result<T>> @this, Func<FailureValue, Task<T>> ifFail) =>
		R.MatchAsync(@this,
			fail: ifFail,
			ok: x => Task.FromResult(x)
		);

	#endregion
}
