using System;
using System.Threading.Tasks;

namespace Wrap.Extensions;

public static partial class ResultExtensions
{
	/// <summary>
	/// Return a value determined by the value of <paramref name="this"/> and the result of <paramref name="predicate"/>.
	/// </summary>
	/// <typeparam name="T">Ok value type.</typeparam>
	/// <param name="this">Result object.</param>
	/// <param name="predicate">Uses value of <paramref name="this"/> if <paramref name="this"/> is <see cref="Ok{T}"/>.</param>
	/// <param name="okFalse">Used if <paramref name="this"/> is <see cref="Ok{T}"/> and <paramref name="predicate"/> returns <see langword="false"/>.</param>
	/// <returns>Original value or value generated by<paramref name="okFalse"/>.</returns>
	public static Result<T> ContinueIf<T>(this Result<T> @this, Func<T, bool> predicate, Func<T, Result<T>> okFalse) =>
		If(@this, predicate, x => x, okFalse);

	/// <inheritdoc cref="ContinueIf{T}(Result{T}, Func{T, bool}, Func{T, Result{T}})"/>
	public static Task<Result<T>> ContinueIfAsync<T>(this Result<T> @this, Func<T, bool> predicate, Func<T, Task<Result<T>>> okFalse) =>
		IfAsync(@this, predicate, x => Task.FromResult(R.Wrap(x)), okFalse);

	/// <inheritdoc cref="ContinueIf{T}(Result{T}, Func{T, bool}, Func{T, Result{T}})"/>
	public static Task<Result<T>> ContinueIfAsync<T>(this Task<Result<T>> @this, Func<T, bool> predicate, Func<T, Result<T>> okFalse) =>
		IfAsync(@this, predicate, x => x, okFalse);

	/// <inheritdoc cref="ContinueIf{T}(Result{T}, Func{T, bool}, Func{T, Result{T}})"/>
	public static Task<Result<T>> ContinueIfAsync<T>(this Task<Result<T>> @this, Func<T, bool> predicate, Func<T, Task<Result<T>>> okFalse) =>
		IfAsync(@this, predicate, x => Task.FromResult(R.Wrap(x)), okFalse);

}
