	/// <summary>
	/// Return a value determined by the value of <paramref name="this"/> and the result of <paramref name="fTest"/>.
	/// </summary>
	/// <typeparam name="T">Ok value type.</typeparam>
	/// <typeparam name="TReturn">Return value type.</typeparam>
	/// <param name="this">Result object.</param>
	/// <param name="fFail">Function to return a value if <paramref name="this"/> is <see cref="Failure"/>.</param>
	/// <param name="fTest">Uses value of <paramref name="this"/> to determines whether <paramref name="fFalse"/> or <paramref name="fTrue"/> should be used.</param>
	/// <param name="fFalse">Used if <paramref name="this"/> is <see cref="Ok{T}"/> and <paramref name="fTest"/> returns <see langword="false"/>.</param>
	/// <param name="fTrue">Used if <paramref name="this"/> is <see cref="Ok{T}"/> and <paramref name="fTest"/> returns <see langword="true"/>.</param>
	/// <returns>Value generated by <paramref name="fFail"/>, <paramref name="fFalse"/> or <paramref name="fTrue"/>.</returns>
	public static TReturn MatchIf<T, TReturn>(this Result<T> @this,
		Func<FailureValue, TReturn> fFail,
		Func<T, bool> fTest,
		Func<T, TReturn> fFalse,
		Func<T, TReturn> fTrue
	) =>
		Match(@this,
			fFail: fFail,
			fOk: x => fTest(x) switch
			{
				false =>
					fFalse(x),

				true =>
					fTrue(x)
			}
		);

	/// <inheritdoc cref="MatchIf{T, TReturn}(Result{T}, Func{FailureValue, TReturn}, Func{T, bool}, Func{T, TReturn}, Func{T, TReturn})"/>
	public static Task<TReturn> MatchIfAsync<T, TReturn>(this Result<T> @this,
		Func<FailureValue, TReturn> fFail,
		Func<T, bool> fTest,
		Func<T, Task<TReturn>> fFalse,
		Func<T, TReturn> fTrue
	) =>
		MatchIfAsync(Task.FromResult(@this),
			fFail: f => Task.FromResult(fFail(f)),
			fTest: x => Task.FromResult(fTest(x)),
			fFalse: fFalse,
			fTrue: x => Task.FromResult(fTrue(x))
		);

