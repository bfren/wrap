using System;
using System.Threading.Tasks;

namespace Wrap.Extensions;

public static partial class ResultExtensions
{
	/// <summary>
	/// Return a value determined by the value of <paramref name="this"/> and the result of <paramref name="fTest"/>.
	/// </summary>
	/// <typeparam name="T">Ok value type.</typeparam>
	/// <typeparam name="TReturn">Return value type.</typeparam>
	/// <param name="this">Result object.</param>
	/// <param name="fTest">Uses value of <paramref name="this"/> to use <paramref name="f"/> or return a Failure object.</param>
	/// <param name="f">Used if <paramref name="this"/> is <see cref="Ok{T}"/> and <paramref name="fTest"/> returns <see langword="true"/>.</param>
	/// <returns>Value generated by <paramref name="f"/>, or a Failure object.</returns>
	public static Result<TReturn> MapIf<T, TReturn>(this Result<T> @this, Func<T, bool> fTest, Func<T, TReturn> f) =>
		If(@this, fTest, x => R.Try(() => f(x)), _ => R.Fail(C.TestFalseMessage).Ctx(nameof(ResultExtensions), nameof(MapIf)));

	/// <inheritdoc cref="MapIf{T, TReturn}(Result{T}, Func{T, bool}, Func{T, TReturn})"/>
	public static Task<Result<TReturn>> MapIfAsync<T, TReturn>(this Result<T> @this, Func<T, bool> fTest, Func<T, Task<TReturn>> f) =>
		MapIfAsync(@this.AsTask(), fTest, f);

	/// <inheritdoc cref="MapIf{T, TReturn}(Result{T}, Func{T, bool}, Func{T, TReturn})"/>
	public static Task<Result<TReturn>> MapIfAsync<T, TReturn>(this Task<Result<T>> @this, Func<T, bool> fTest, Func<T, TReturn> f) =>
		MapIfAsync(@this, fTest, async x => f(x));

	/// <inheritdoc cref="MapIf{T, TReturn}(Result{T}, Func{T, bool}, Func{T, TReturn})"/>
	public static Task<Result<TReturn>> MapIfAsync<T, TReturn>(this Task<Result<T>> @this, Func<T, bool> fTest, Func<T, Task<TReturn>> f) =>
		IfAsync(@this, fTest, x => R.TryAsync(async () => await f(x)), async _ => R.Fail(C.TestFalseMessage).Ctx(nameof(ResultExtensions), nameof(MapIfAsync)));
}
